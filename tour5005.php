				<p><strong>THIRUVANNAMALAI HILL AND ITS GREATNESS…</strong></p>
				<p>Known for its beautiful temples and heritage sites that date back to the 9th century, Its history revolves around the famous Annamalaiyar Temple, which houses some of the rarest inscriptions from ancient times.
				</p>
				<p><strong>The History and Impact of Siddhargals</strong></p>
				<p>The Kings of South India from the ancient times, have given great importance to Tiruvannamalai and the temple of Arunachala. For a thousand years, these kings have built prakaras outlying temples, gopurams and compound walls to commemorate the gods. They also dug ponds, offered jewels and donated gold for the well-being of the temple lands.
				</p>
				<p>Many ancient sages from the Himalayas and other sacred places of India came to Arunachala, either on a pilgrimage trip or to live in its vicinity. The notable ones like Valmiki and and a few others gave us the itihasas. Vyasa, Narada and some others gave us the Puranas. While Apastamba, Bodayana, Yajnavalkya, Parasara, Jaimini and others gave us portions of the Vedas. Pipalatha and others gave us the Upanishads. All of these great sages were attracted by the magnetic power of Arunachala of Tiruvannamalai.
				</p>
				<span><strong>Deiva Sigamani Desika Paramacharya Swamigal</strong></span> lived in Arunachalam about 700 years ago. He brought back king’s loving horse to life by praying to Lord Arunachala and tapping the horse’s body with his cane. He asked the King to undertake the renovation of the Arunachaleswara temple. The King inscribed the story of the miracle performed by the saint on the south Wall of the third prakara of the temple.
				</p>
				<span><strong>Mangaiyarkarasi -</strong></span> Once when the people of Tiruvannamalai suffered from water scarcity, the saint constructed a large tank here. Her generous deeds jewels etc are lauded in the inscriptions on the walls of the temple prakara. Pani Pathraswami - The real name of Pani Pathraswami was Giri Deva. When he wished to take water to the temple, the water from the Brahma Thirtham came out of its own accord in his hands in the form of a vessel. It is said Arunachaleswara gave him a gold coin everyday to help him feed His devotees. Dakshinamurthi Swami - He did not speak until he was five. An ascetic told his parents, “Talk to him, he will speak now”. He spoke spirituality from his young age unimaginably. Ammani Ammaiyar - She had the gift of curing diseases by giving holy ash. Angapradakshinam Annamalai Swami - He was thus called because all his life he worshipped Arunachalaby performing Angapradakshina that is rolling on the ground in an anticlockwise direction round the hill. Irai Swamigal - he would recite Arunachala’s name 108 times for each step he took while circumambulating the hill of 14 kms. Another well-known sage, Guhai Namasivayar, brought back a dead sheep to life. Knowing of this, some people who were skeptical wanted to test his powers. They lay a healthy man on a bamboo pad and had him pretend to be dead. They then took him to Guhai Namasivaya and pleaded to bring him back alive. Namasivaya, through his powers, knew their trick. Annoyed by their act, he declared that the man was truly dead. Frightened by this, the folks prayed and requested Lord Arunachalam to have mercy on them. Upon his grace, the man was brought back to life.
				<p>Two of the illustrious disciples of Guhai Namasivaya were VirupakshiThevar and Guru Namasivayar. The former lived just above the cave of Guhai Namasivayar. At his death, his body naturally reduced to ashes. After about 400 years in the beginning of the 20th century these ashes were made into the form of a linga by Bhagavan Ramana himself during his stay at Virupakshi cave. Arunagirinathar - Saint Arunagirinathar and Lord Muruga are inseparable, like Saivism and Tamil. When his sister scolded him for his conduct, he felt hurt and tried to kill himself by falling from the top of the Vallala Maharaja Gopuram in the temple. Lord Muruga stopped Him and blessed him with spiritual knowledge.
				</p>
				<p>Veera Vairagya Murthy Swami - During the Karthigai festival in Tiruvannamalai, he restored to life a cow slaughtered by a Muslim King. He also made the stone Nandi idol of bull to graze on grass. The Muslim King was amazed at his powers and praised the glory of Arunachaleswara. Ramana And Seshadri - In 1896 Bhagavan Ramana came to Tiruvannamalai and immersed in deep samadhi inside the Pathala linga in the temple. Ramana smilingly said that there were three mad men in Arunachala. One was Seshadri, the second was Arunachaleswara and the third was himself. Seshadri performed many miracles to relieve the sufferings of his devotees. Sri Isanya Jnana Desikar Mutt & Samadhi Temple - It is said that tigers used to keep guard around this sage when he was absorbed in meditation. He attained Mahasamadhi in 1829. Two tigers sat close to him always.
				</p>

				<p><strong>Holy Caves of Tiruvannamalai</strong></p>
				<p>There are many caves in Arunachala. There are caves formed by two or three rocks which can accommodate only a single person and there are huge caves made of massive rocks where many people can live comfortably. There are also mysterious caves which are beyond exploration. These vast spaces inside the holy hill contain delectable sights. The Puranas and even yogis have confirmed, that from these mysterious caves, one can watch the wonderful sights and also see siddhas deep in samadhi, oblivious to the world and content with partaking and absorbing the abundant grace of Arunachala.
				</p>
				<p><strong>Girivalam</strong></p>
				<p>The Holy Mountain of Arunachala rises up to an elevation of 2669 feet and has an encircling road which is 14 kms in circumference. On Full Moon days and festival days like Karthigai and Tamil New Year day, thousands of devotees undertake girivalam i.e. circumambulating the mountain to get the blessings of the Lord Arunachala. This hill is made up of igneous rock connected with fire, which is one of the four kinds of mountains as classified by geology. The uniqueness of this hill is also backed by science. An American Geologist has stated that this mountain Arunachala should have been destructed by the earth under the stress of some violent volcanic eruption in the Dim Ages before even the coal-bearing layers were formed. It existed long before the gigantic saurians of the pre-historic world, moved its ungainly forms through the prehistoric forests that covered our early earth. Such a barren rock has captivated the souls of sages and saints for ages together.
				</p>
				<p>Legends say that Tiruvannamalai was a fire alias Agni Mountain in Krithayuga, Goldalias Swarna Mountain in Threthayugha, Copper alias Thambra Mountain in Dwaparayuga and Rock Mountain in this Kaliyuga. Tamil legends mention that it is far more ancient than the Himalayas. One will surely attain liberation … By seeing Chidambaram, by being born in Tiruvarur, by dying in Kasi, by merely thinking of Arunachaleswarar hill of Thiruvannamalai. Arunachala is a combination of two Sanskrit words Aruna and Achala. Aruna means red and Achala means immovable mount. Therefore, it is also known as the Red Mount. There are eight Lingas, eight Nandis, more than 350 tanks and many mandapams around the hill. The eight Lingams are Indira Lingams, Agni Lingam, Ema Lingam, Niruthi Lingam, Varuna Lingam, Vaayu Lingam, Kubera Lingam and Esanya Lingam.
				</p>
				<p><strong>Karthigai Deepam Festival</strong></p>
				<p>Brahma and Vishnu were both quarrelling about their relative superiority. To give a decision on it and to erase their ego, Lord Shiva stood before them in the form of a blazing mountain. Having failed in their attempt to find out its top and bottom, the two prayed to Lord Shiva for his grace. He blessed them, stating that he would appear in the form of Jothi on the top of the hill every year on the day of Karthigai in the tamil month of Karthigai. To commemorate this event, the Deepam festival is being conducted at Tiruvannamalai every year.
				</p>
				<p>There is ample literary evidence to prove that this lighting festival is one of the oldest in Tamil culture. Any one can see the literary for themselves and be amazed by the inscriptions and evidence of the existence of the hill. As per the data, the temple is evidentially older than 3000 yrs. One of the oldest tamil literaries, Tholkappiam, which is roughly 3000 years old, also mentions the Deepam festival. Jeevaka Chintamani, one of the five great Tamil Epics mentions lighting a lamp on the hill on Karthigai day. Saints Appar and Sambandar who hailed in the 7th century A.D. (1300 years ago) make references about Karthigai Deepam. 
				</p>
