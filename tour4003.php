				<p>A village is a clustered human settlement or community, larger than a hamlet but smaller than a town, with a population ranging from a few hundred to a few thousand. Villages are normally permanent, with fixed dwellings. Further, the dwellings of a village are fairly not very closer to one another, not scattered broadly over the landscape, as a dispersed settlement.
