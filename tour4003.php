				<p><strong>CATTLE FARMING:</strong></p>
				 
				<p>It is the act of raising and breeding cattle for the production of milk. Besides being nutritious and delicious and a nutrient dense food, cattle provide over 900 by products that are used by EVERYONE. They contribute to the roads we drive on, medicinal and pharmaceuticals, leather and other products, shampoos and soaps and lotions, even the tires on our cars. There is so much we get from cows; Cattle also act as fire retardants in brushy area and can reduce the increasingly dangerous "fire load" that we all see in the West. If we don't let fires burn (naturally), then we should allow cows to graze, on marginal lands where the brush and undergrowth thrives. As a result, there is nothing for grazing for deer and other wildlife and the brush SUCKS up water faster than people can imagine. If water, fire, and open land is of importance to people, then they should learn to appreciate and understand the incredible contributions cattle -- who co-exist with wildlife and waterfowl and rivers,streams, etc. -- without harming them! The misinformation abounds about cattle, which is terribly sad because they can contribute so much environmentally to our lives. 
